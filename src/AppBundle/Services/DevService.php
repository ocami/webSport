    public function request($action){
        $stmt = '';

       switch ($action){

           case 'initialize' :
               $stmt = $this->initialize($stmt);

           case 'delete-organizer' :
               $stmt = $this->deleteOrganizer($stmt);

           case 'delete-competitions' :
               $stmt = $this->deleteCompetitions($stmt);

           case 'delete-races' :
               $stmt = $this->deleteRaces($stmt);

           case 'delete-rank' :
               $stmt = $this->deleteRaceRank($stmt);
       }

        return $this->stmt($stmt);
    }

    private function stmt($sql)
    {
        $conn = $this->em->getConnection();

        $stmt = $conn->prepare($sql);
        $stmt = $stmt->execute();

        if ($stmt)
            return $this->message;
        else
            return false;
    }

